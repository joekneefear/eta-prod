#
# pack_PTR
#
# Same interface and similar functionality of routine generated by stdf_gen
# run with stdfPL.spec.  Intended to over-ride stdf_gen pack_PTR 
# routine.
#
# Record is assumed to be only truncated at option_flg or just after option_flg
# stdf_gen will truncate anywhere field is not defined.
#
# Performance enhancement. Ptr's are bulk of datalog file.
# In line code and minimized pack calls for speed.
#
# Not as much error checking as pack_PTR from stdf_gen
#
sub pack_PTR
{
my $ptr = shift ;
my $header ;
my $record ;
#
# convert $test_flg to B*1
#
my $num_of_pad_bits ; # variable, gets reused
my $test_flg =  $$ptr{test_flg} ;
$test_flg =~ s/^0+// ; # strip off leading sig bits of 0
$num_of_pad_bits = 8-length($test_flg) ;
($num_of_pad_bits >= 0 ) || confess_pack("ptr{test_flg} bit overflow\n" );
$test_flg = "0"x$num_of_pad_bits . $test_flg ;
#
# convert $parm_flg to B*1
#
my $parm_flg = $$ptr{parm_flg} ;
$parm_flg =~ s/^0+// ; # strip off leading sig bits of 0
$num_of_pad_bits = 8-length($parm_flg) ;
($num_of_pad_bits >= 0 ) || confess_pack("ptr{parm_flg} bit overflow\n" );
$parm_flg = "0"x$num_of_pad_bits . $parm_flg ;
#
# check result for infinity
# and Fix
#
my $result = $$ptr{'result'} ;
  (($result eq '-Infinity')
    || ($result eq '-NaN')
    || ($result eq '-1.#INF' )
    || ($result < $::FLT_MAX_NEG_CHECK ))
    && ($result = -$::FLT_MAX) ;
  ((${$ptr}{result} eq 'Infinity')
    || ($result eq 'NaN')
    || ($result eq '1.#INF' )
    || ($result > $::FLT_MAX_POS_CHECK ))
    && ($result = $::FLT_MAX) ;

$record = pack(
  $::U4.$::U1.$::U1.$::B1.$::B1.$::R4,
  ${$ptr}{test_num},
  ${$ptr}{head_num},
  ${$ptr}{site_num},
  $test_flg,
  $parm_flg,
  $result) ;

#
# truncate ptr if opt_flag is not defined
# or if %ENV{STDF_PERL_TRUN_PTR} is set
#
if (defined($$ptr{opt_flag}) && !$stdf_perl_trun_ptr)
  {
  #
  # convert $opt_flg to B*1
  #
  my $opt_flg = $$ptr{opt_flag} ;
  $opt_flg =~ s/^0+// ; # strip off leading sig bits of 0
  $num_of_pad_bits = 8-length($opt_flg) ;
  ($num_of_pad_bits >= 0 ) || confess_pack("ptr{opt_flg} bit overflow\n" );
  $opt_flg = "0"x$num_of_pad_bits . $opt_flg ;
  $record .= pack(
    $::B1,
    $opt_flg) ;
  if (defined($$ptr{res_scal}))
    {
    #
    # convert $desc_flg to B*1
    #
    my $desc_flg = $$ptr{desc_flg} ;
    $desc_flg =~ s/^0+// ; # strip off leading sig bits of 0
    $num_of_pad_bits = 8-length($desc_flg) ;
    ($num_of_pad_bits >= 0 ) || confess_pack("ptr{desc_flg} bit overflow\n" );
    $desc_flg = "0"x$num_of_pad_bits . $desc_flg ;

    #
    # find the length of all the strings C*n
    #
    $test_nam = $$ptr{test_nam} ;
    $test_nam =~ s/ +$// ; # remove trailing spaces
    ( length($test_nam) > 255 ) && ($test_nam = substr($test_nam,0,254)) ;
    $test_nam_len = length( $test_nam ) ;

    $seq_nam = $$ptr{seq_name} ;
    $seq_nam =~ s/ +$// ; # remove trailing spaces
    ( length($seq_nam) > 255 ) && ($seq_nam = substr($seq_nam,0,254)) ;
    $seq_nam_len = length( $seq_nam ) ;

    $test_txt = $$ptr{test_txt} ;
    $test_txt =~ s/ +$// ; # remove trailing spaces
    ( length($test_txt) > 255 ) && ($test_txt = substr($test_txt,0,254)) ;
    $test_txt_len = length( $test_txt ) ;
    
    #
    # check R*4s for Infinity -Infinity and fix
    #
    my $lo_limit = $$ptr{lo_limit} ;
      (($lo_limit eq '-Infinity')
        || ($lo_limit eq '-NaN')
        || ($lo_limit eq '-1.#INF' )
        || ($lo_limit < $::FLT_MAX_NEG_CHECK ))
        && ($lo_limit = -$::FLT_MAX) ;
    my $hi_limit = $$ptr{hi_limit} ;
      (($hi_limit eq 'Infinity')
        || ($hi_limit eq 'NaN')
        || ($hi_limit eq '1.#INF' )
        || ($hi_limit > $::FLT_MAX_POS_CHECK ))
        && ($hi_limit = $::FLT_MAX) ;

    $record .= pack(
    $::I1.$::U1.$::U1.$::B1.$::C7.$::I1.$::I1.$::U1.$::U1.$::U1.$::U1.$::R4.$::R4.$::U1.$::C.$test_nam_len.$::U1.$::C.$seq_nam_len.$::U1.$::C.$test_txt_len,
    $$ptr{res_scal},
    $$ptr{res_ldig},
    $$ptr{res_rdig},
    $desc_flg,
    $$ptr{units},
    $$ptr{llm_scal},
    $$ptr{hlm_scal},
    $$ptr{llm_ldig},
    $$ptr{llm_rdig},
    $$ptr{hlm_ldig},
    $$ptr{hlm_rdig},
    $lo_limit,
    $hi_limit,
    $test_nam_len,
    $test_nam,
    $seq_nam_len,
    $seq_nam,
    $test_txt_len,
    $test_txt ) ;
    }
  }
$$ptr{rec_len} = length($record) ;
$header = pack ( $::U2.$::U1.$::U1,
  $$ptr{rec_len},
  15,
  10);
  
return($header . $record) ;
}

$stdf_max_wmr_physical_die_bins = 32000 ;	# set equal to STDF_MAX_WMR_PHYSICAL_DIE_BINS in C libraries (used by edbtdl)
$stdf_max_wmr_die_bins = 256000 ;			# set equal to STDF_MAX_WMR_DIE_BINS in C libraries (used by edbtdl)

sub unpack_WMR
{
my $record = shift ;
my $wmr = shift ;
my $i = 0 ;
my $rc ;
my $len = length($$record) ;

undef($$wmr{rec_len});
undef($$wmr{rec_typ});
undef($$wmr{rec_sub});
undef($$wmr{die_cnt});
undef($$wmr{die_bin});

do
  {
  $rc = &::stdf_unpack(
    \'U*2 U*1 U*1 I*2',
    $record,
    \$i,
    \$$wmr{rec_len},
    \$$wmr{rec_typ},
    \$$wmr{rec_sub},
    \$$wmr{die_cnt});
  $$wmr{die_bin} .= substr($$record,$i,$$wmr{die_cnt}) ;
  $i += $$wmr{die_cnt} ;  # increment pointer
  } while ($i < $len) ;

$$wmr{die_cnt} = length($$wmr{die_bin}) ;
return (0) ;
}

#
# takes die_bin as a string, or an array of numbers >0 < 255
#
sub pack_WMR
{
my $wmr = shift ;
my $header ;
my $record='' ;
my $rec_len ;

my $wmr_die_bin ;

if ( $$wmr{die_bin} =~ /^ARRAY[(].*[)]$/ )
  {
  #
  # if die_bin is an array, convert it to a string
  # probably faster for user to do this
  # this is mostly for the use of stdf_copy_ascii
  #
  my $die ;
  my $i=0 ;
  foreach $die ( @{$$wmr{die_bin}} )
    {
    (($die < 256) && ($die >= 0 )) ||
      (&::wlog(0,"Warning:  pack_WMR:  WMR.DIE_BIN[$i]=$die, out of bounds\n")) ;
    $wmr_die_bin .= pack("C",$die) ;
    $i++ ;
    }
  }
else
  {
  $wmr_die_bin = $$wmr{die_bin} ;
  }

$$wmr{die_cnt} = length($wmr_die_bin) ;
my $die_cnt = $$wmr{die_cnt} ;

while ( $die_cnt > 0 )
  {
  if ( $die_cnt > $stdf_max_wmr_physical_die_bins )
    {
    #
    # We have greater than 32k wafer data left to write
    #
    $rec_len = $stdf_max_wmr_physical_die_bins + 2 ;
    $record .= pack( $::U2.$::U1.$::U1.$::I2,
                   $rec_len,
	           105,
	           200,
                   $stdf_max_wmr_physical_die_bins)
                  .substr($wmr_die_bin,0,$stdf_max_wmr_physical_die_bins) ;
		  $wmr_die_bin = substr($wmr_die_bin,$stdf_max_wmr_physical_die_bins) ;
		  $die_cnt = $die_cnt - $stdf_max_wmr_physical_die_bins ;
    }
  else
    {
    #
    # We have less than 32k wafer data left to write
    #
    $rec_len = $die_cnt + 2 ;
    $record .= pack( $::U2.$::U1.$::U1.$::I2,
                   $rec_len,
	           105,
	           200,
                   $die_cnt)
                   .$wmr_die_bin ;
    $len = length($wmr_die_bin) ;
    if ( $die_cnt !=  $len )
      {
      &::wlog(0,"pack_WMR:  error:  die_count left over = $die_cnt, map data left over len(map_data) = $len\n") ;
      }
    $die_cnt = 0 ;
    }
  }
#
# null terminating WMR record with 0 die count
# STDF+ Version >= 202
#
$record .= pack( $::U2.$::U1.$::U1.$::I2,
                     2, 
                   105,
                   200,
                   0) ;
return($record) ;
}

