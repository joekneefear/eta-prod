#
# update_EMIR
#
# Function to update EMIR record
# Users responsibility to make sure that record is same length
# Checks are made and reported.
#
# File being updated must be closed
#
use POSIX ("close","write") ;  # ?? open
sub update_EMIR
{
my $emir=shift ;    # reference to emir record
my $out_fn=shift ;  # actual file name
my $rec_len = 0 ;

my $emir_bin = &pack_EMIR($emir) ;

{
local *EMIR_INPUT ;
my $status ;
my $rec_typ ;
my $rec_sub ;
my $buf ;
my $stdf_ver ;
if (! open EMIR_INPUT, "<$out_fn")
  {
  &::wlog(0,"update_EMIR:  Could not open $out_fn\n") ;
  return(-2) ;
  }
binmode EMIR_INPUT ;
($status, $rec_len, $rec_typ, $rec_sub, $buf) = &::read_first_rec( \*EMIR_INPUT, $stdf_ver ) ;
close EMIR_INPUT ;
my $len = length($emir_bin) ;
if (${$emir}{rec_len} != $rec_len)
  {
  &::wlog(0,"update_EMIR:  Could not update EMIR, record lengths don't match; file=$rec_len, update=$len \n") ;
  return(-1) ;
  }
}

if (! ($output=POSIX::open($out_fn, &POSIX::O_RDWR)))
  {
  &::wlog(0,"update_EMIR:  Could not open $out_fn for update_EMIR\n") ;
  return(-2) ;
  }

if (! POSIX::write($output, $emir_bin, length($emir_bin)))
  {
  &::wlog(0,"update_EMIR:  Could not write EMIR counts\n") ;
  return(-3) ;
  }

if ( ! POSIX::close( $output ))
  {
  &::wlog(0,"update_EMIR:  Could not POSIX::close $out_fn \n") ;
  return(-3) ;
  }

return(0) ;
}

